    private void gameStartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gameStartMouseClicked
        if (gameStart.isEnabled()) {
            gameStart.setEnabled(false);
            gameReset.setEnabled(false);
            gameStop.setEnabled(true);
            paused = false;
            T1 t1 = new T1();
            Thread th1 = new Thread(t1);
            th1.start();
            T2 t2 = new T2();
            Thread th2 = new Thread(t2);
            th2.start();
        }

    }//GEN-LAST:event_gameStartMouseClicked

    private void gameStopMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gameStopMouseClicked
        if (gameStop.isEnabled()) {
            gameStart.setEnabled(true);
            gameReset.setEnabled(true);
            gameStop.setEnabled(false);
            paused = true;
        }
    }//GEN-LAST:event_gameStopMouseClicked

    private void shotResetMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_shotResetMouseClicked
        if (gameClock.getText().contains(".")){
            String[] str = gameClock.getText().split("\\.");
            if(Integer.parseInt(str[0])<24){
                shotClock.setText(""); //disable shot clock if game clock below 24s
            } else {
                shotClock.setText("24");
            }
        } else {
            shotClock.setText("24");
        }
        shotClock.setForeground(Color.yellow);
    }//GEN-LAST:event_shotResetMouseClicked

    private void gameResetMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gameResetMouseClicked
        if (gameReset.isEnabled()) {
            gameClock.setText(qtrLength + ":00");
            shotClock.setText("24");
            gameClock.setForeground(Color.yellow);
            shotClock.setForeground(Color.yellow);
            gameReset.setEnabled(false);
        }
    }//GEN-LAST:event_gameResetMouseClicked

