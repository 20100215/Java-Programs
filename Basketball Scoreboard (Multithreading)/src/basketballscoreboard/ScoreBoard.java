    class T1 implements Runnable { //game clock

        private void sleep(long milis) {
            try {
                Thread.sleep(milis);
            } catch (InterruptedException e) {
                System.out.println(e);
            }
        }

        public synchronized void run() {
            while (paused == false && !gameClock.getText().equals("0.0")) {
                if (gameClock.getText().contains(":")) {
                    
                    String[] str = gameClock.getText().split(":");
                    int min = Integer.parseInt(str[0]);
                    int sec = Integer.parseInt(str[1]) - 1;
                    if (sec < 0) {
                        min -= 1;
                        sec = 59;
                    }
                    if (sec < 10) {
                        gameClock.setText(min + ":0" + sec);
                    } else {
                        gameClock.setText(min + ":" + sec);
                    }
                    if (min < 1) {
                        gameClock.setText("59.9");
                        continue;
                    }
                    sleep(1000);
                    
                } else {
                    
                    String[] str = gameClock.getText().split("\\.");
                    int sec = Integer.parseInt(str[0]);
                    int ms = Integer.parseInt(str[1]) - 1;
                    if (ms < 0) {
                        sec -= 1;
                        ms = 9;
                    }
                    if (sec < 0) {
                        gameClock.setText("0.0");
                        gameStart.setEnabled(false);
                    } else {
                        gameClock.setText(sec + "." + ms);
                    }
                    sleep(100);
                    
                }
            }
            if (gameClock.getText().equals("0.0")){
                gameClock.setForeground(Color.red);
            }
        }
    }

