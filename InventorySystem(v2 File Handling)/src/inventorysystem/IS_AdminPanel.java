    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        /*Prompt user to confirm item deletion, then remove from database and refresh table*/
        int del = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete the selected item record ?");
        if (del == 0) {
            int id = Integer.parseInt(itemIdField.getText());
            f.deleteItem(id);
            JOptionPane.showMessageDialog(null, "Item record deleted!");
            editItemPanel.setVisible(false); //hides the edit item components
            clickAnItemLabel.setVisible(true);
            refresh();
        }
    }//GEN-LAST:event_jButton3MouseClicked

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();

        /*setting values to the update item tab*/
        itemIdField.setText(model.getValueAt(row, 0).toString());
        itemBrandField.setText(model.getValueAt(row, 2).toString());
        itemNameField.setText(model.getValueAt(row, 1).toString());
        categoryField.setSelectedItem(model.getValueAt(row, 3).toString());
        qtyRemField.setText(model.getValueAt(row, 4).toString());
        qtyMaintainField.setText(model.getValueAt(row, 5).toString());
        storeLocField.setText(model.getValueAt(row, 6).toString());

        if (Integer.parseInt(qtyRemField.getText()) > 0) {
            qtyRemMinus.setEnabled(true);
        }
        if (Integer.parseInt(qtyMaintainField.getText()) > 0) {
            qtyMaintainMinus.setEnabled(true);
        }

        editItemPanel.setVisible(true);
        clickAnItemLabel.setVisible(false);
    }//GEN-LAST:event_jTable1MouseClicked

