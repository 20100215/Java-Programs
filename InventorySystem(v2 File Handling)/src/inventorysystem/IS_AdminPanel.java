    /**
     * @param args the command line arguments
     */
    public void displayTable(String[][] list) {
        try {
            int x;
            for(x=0; list[x][0]!=null; x++) {

                Object[] row = {x+1, list[x][0], list[x][1], list[x][2], list[x][3], list[x][4], list[x][5]};

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

                model.addRow(row);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    public void refresh() {
        /*display table - original form*/
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        String[][] list = f.displayItems();
        displayTable(list);

        /*reset search bar*/
        searchBar.setText("");

        /*reset update tab*/
        qtyRemMinus.setEnabled(false);
        qtyMaintainMinus.setEnabled(false);

        /*reset insert tab*/
        itemBrandField1.setText("");
        itemNameField1.setText("");
        categoryField1.setSelectedIndex(0);
        qtyRemField1.setText("0");
        qtyMaintainField1.setText("0");
        storeLocField1.setText("");
        qtyRemMinus1.setEnabled(false);
        qtyMaintainMinus1.setEnabled(false);
        
        editItemPanel.setVisible(false);
        clickAnItemLabel.setVisible(true);
    }

