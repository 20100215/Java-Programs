    private void searchButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchButtonMouseClicked
        String keyword = searchBar.getText();
        if (!"".equals(keyword)) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            String[][] list = f.searchItems(keyword);
            displayTable(list);            
        } else {
            JOptionPane.showMessageDialog(null, "Please enter keywords to search");
        }
    }//GEN-LAST:event_searchButtonMouseClicked

    private void confirmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmMouseClicked
        /*update item*/
        int id = Integer.parseInt(itemIdField.getText());
        String[] item = new String[6];
        item[0] = itemNameField.getText();
        item[1] = itemBrandField.getText();
        item[2] = categoryField.getSelectedItem().toString();
        item[3] = qtyRemField.getText();
        item[4] = qtyMaintainField.getText();
        item[5] = storeLocField.getText();


        if (!"".equals(item[0]) && !"".equals(item[1]) && !"".equals(item[2])) {
            f.updateItem(id,item);
            JOptionPane.showMessageDialog(null, "Item record successfully updated!");
            refresh();
        } else {
            JOptionPane.showMessageDialog(null, "Please fill in all information");
        }
    }//GEN-LAST:event_confirmMouseClicked

