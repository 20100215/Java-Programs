    private void confirm1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirm1MouseClicked
        /*insert item*/
        String[] item = new String[6];
        item[0] = itemNameField1.getText();
        item[1] = itemBrandField1.getText();
        item[2] = categoryField1.getSelectedItem().toString();
        item[3] = qtyRemField1.getText();
        item[4] = qtyMaintainField1.getText();
        item[5] = storeLocField1.getText();


        if (!"".equals(item[0]) && !"".equals(item[1]) && !"".equals(item[2])) {
            f.insertItem(item);
            JOptionPane.showMessageDialog(null, "Item record successfully inserted!");
            refresh();
        } else {
            JOptionPane.showMessageDialog(null, "Please fill in all information");
        }
    }//GEN-LAST:event_confirm1MouseClicked

